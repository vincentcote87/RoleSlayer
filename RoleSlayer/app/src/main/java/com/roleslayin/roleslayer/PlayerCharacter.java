package com.roleslayin.roleslayer;

public class PlayerCharacter {
    String name;
    String info;

    void setName(String name) {
        this.name = name;
    }
    void setInfo(String info) {
        this.info = info;
    }

    String getName() {
        return name;
    }
    String getInfo() {
        return info;
    }
}
